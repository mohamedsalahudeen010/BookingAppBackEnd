import mongoose from "mongoose";
import jwt from "jsonwebtoken"
const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    country: {
      type: String,
      required: true,
    },
    img: {
      type: String,
    },
    city: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    }
  },
  { timestamps: true }
);

const genUserAuthToken=(id)=>{
  return jwt.sign({id},process.env.SECRET_CODE_USER)
}

const User= mongoose.model("User", UserSchema);

export{User,genUserAuthToken}